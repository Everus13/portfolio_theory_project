import re

def clean_description(text: str) -> str:
    """
    Приводит текст к нижнему регистру, удаляет номера карт,
    даты, спецсимволы и лишние пробелы.
    """
    if not isinstance(text, str):
        return ""
        
    text = text.lower()
    
    # Удаляем номера карт (например, *1234 или 4276********1234)
    text = re.sub(r'\*+\d+', '', text)
    text = re.sub(r'\d{4,}\*+\d+', '', text)
    
    # Удаляем даты (например, 12.05, 12/05)
    text = re.sub(r'\b\d{1,2}[\./]\d{1,2}\b', '', text)
    
    # Удаляем все символы, кроме букв (русских и английских), цифр и пробелов
    text = re.sub(r'[^a-zа-яё0-9\s]', ' ', text)
    
    # Заменяем множественные пробелы на один и удаляем пробелы по краям
    text = re.sub(r'\s+', ' ', text).strip()
    
    return text
